# Creating dictionary using dict()


# Empty Dictionary Created

a=dict()

print(a, type(a))

# Crating Dictionay using List

b=dict([("jay",80),("chetan",85),("raj",90),("ashish",95)])
print(b, type(b))

# Crating Dictionay using Tuple

c=dict((("jay",80),("chetan",85),("raj",90),("ashish",95)))
print(c, type(c))


# Crating Dictionay using Tuple

d=dict(({"jay",80},{"chetan",85},{"raj",90},{"ashish",95}))
print(c, type(d))

# Crating Dictionay using Set

e=dict({("jay",80),("chetan",85),("raj",90),("ashish",95)})
print(e, type(e))



