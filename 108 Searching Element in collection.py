# Searching element in collection

# Search element in str

x="Python is simple"

print("Python" in x) #True

# Search element in List

x=[10,20,30,40]

print(10 in x) #True
print(60 not in x) # True
print(60 in x) # False


# Search element in Tuple

x=(60,70,80,90)
print(70 in x)# True
print(100 not in x) # True
print(100 in x) # False

# Search element in Set

x={100,200,300,400}

print(200 in x) # True
print(600 not in x) # True
print(600 in x) # False




