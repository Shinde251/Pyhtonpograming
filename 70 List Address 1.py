# List Addresses :-


x=[10,10,20,40,10]

print("Accessing List elements using Forward Index")
print(x[0],id(x[0]))
print(x[1],id(x[1]))
print(x[2],id(x[2]))
print(x[3],id(x[3]))
print(x[4],id(x[4]))

print("\nAccessing List elements using Backward Index")

print(x[-5], id(x[-5]))
print(x[-4], id(x[-4]))
print(x[-3], id(x[-3]))
print(x[-2], id(x[-2]))
print(x[-1], id(x[-1]))





    
