# Set Creation using {}


# Homogeneous Set

a={10,20,30,40}

print(a, type(a),id(a))


# Hetrogenous Set

b={10,3.5,"VIP",True}

print(b, type(b),id(b))

# Set does not allow duplicate elements

c={10,10,10,10,20}

print(c, type(c))


# Set does not Preserve Insertion Order

d={"chetan","Jay","Nikita","Bhageshri","Ashish"}

print(d, type(d))










