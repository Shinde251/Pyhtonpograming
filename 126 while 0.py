# while :- Executing a set of stmts as long as given condition is True.
'''
Syntax:

Initialization

while condition :
    stmts
    increament/decreament


All the line within while block should follow space indentation.
'''

i=1                               # initialization

while i<=10 :                     # condition
    print("hi . . .  . .")
    i=i+1                         # step

print("end")                      # defeault stmts executes only once
