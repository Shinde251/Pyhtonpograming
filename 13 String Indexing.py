# String support indexing

#String support both Forward/Positive Index and Backward /Negative Index


x="python"


print("\nForward Index \n")
print(x[0],type(x[0]), id(x[0]))
print(x[1])
print(x[2])
print(x[3])
print(x[4])
print(x[5])


print("\n\nBackward Index \n")
print(x[-6], type(x[-6]), id(x[-6]))
print(x[-5])
print(x[-4])
print(x[-3])
print(x[-2])
print(x[-1])





