# String Special Oprators

# in or not in :- returns either True or False

x="Python is simple"



print("p" in x)  # True

print("Python" in x)  # True

print("jay" in "jay chetan nikita bhageshri priyanka") # True


print("Ramesh" in "jay chetan nikita bhageshri priyanka") # False

print("Ramesh" not in "jay chetan nikita bhageshri priyanka") # True
