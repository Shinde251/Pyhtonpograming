# Identity Operators :-
'''
These operators are used to compaires addresses of two varaibles.

These operators return either True / False

If two varaibles addresses are same then return True

If two varaible addresses are different then return False

Types of Identity Operators

1) is

2) is not

'''

print(10 is 10) # True

print(10 is 20) # False

print(10 is not 20) # True

s1="VIP"
s2="Python"

print(s1 is s2) # False

print(s1 is "VIP") # True


x=100
y=100

print(x is y) # True












