# List Indexing :-

#List supports both Forward and Backwad indexes.

#Every element in list can be accessed using unique index

x=[10,20,30,40,50]

print("Accessing List elements using Forward Index")
print(x[0])
print(x[1])
print(x[2])
print(x[3])
print(x[4])

print("\nAccessing List elements using Backward Index")

print(x[-5])
print(x[-4])
print(x[-3])
print(x[-2])
print(x[-1])





    
