# Membership Operators

'''
These operators are used to check whether content is available or not in
collection or sequence.

These operators always return True or False

2 Types of Membership Operators

1) in

2) not in

'''




print('P' in 'Python') # True

x='VIP Python Programming Institute'

print('VIP' in x) # True

print('jay' in 'chetan raj jay ram') # True


print('jay' not in 'chetan raj jay ram') # False










