# Creating Tuple using Parenthesis


a=()  # empty tuple

print(a, type(a),id(a),len(a))


b=(10,20,30,40,50) # Homogeneous Tuple

print(b, type(b), id(b), len(b))


c=(10,3.5,"VIP",True) # Hetrogeneous Tuple
print(c, type(c), id(c), len(c))

d=(10,20,10,10,30) # Duplicate elements Tuple

print(d, type(d),id(d),len(d))

e=100,200,300,400  # Tuple can be crated without parenthesis

print(e, type(e), id(e))












