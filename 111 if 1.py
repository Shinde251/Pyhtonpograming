
#if :-
'''
    This is one way decision stmt.

    Syntax:-

    if (condition ):        ---> if condition
        stmt1
        stmt2
        stmt3             ----> if body
        stmt4

All the lines within if body should follow space Indentation.
in the above syntax if given condition is True then if block will be get
executed. '''



n=int(input("Enter any number : "))

if(n<=4):            # if condition
    print("one")
    print("two")
    print("three")   #if body
    print("four")

print("\n End")      # everytime this stmt executes only once by default 






