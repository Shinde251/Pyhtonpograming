# Typecasting :- Converting one datatype to another datatype .
# int to other types conversion
x=10

print(x,type(x))


# Converting int to float

y=float(x)

print(y, type(y))

# Converting int to str


z=str(x)

print(z, type(z))

# Coverting int bool

p=bool(x)


print(p, type(p))


# any non-zero (positive or negative) number is equivalant to true(in bool case)
# zero is equivalant to false



q=bool(0)
print(q, type(q))





